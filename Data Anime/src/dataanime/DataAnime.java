    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(!txtId.getText().equals("")){
            JOptionPane.showMessageDialog(null, 
                    "Silahkan refresh terlebih dahulu!");
        }
        else{
            String judul = txtJudul.getText();
            String tipe = txtTipe.getText();
            String episode = txtEpisode.getText();
            String genre = txtGenre.getText();
            String status = (String) cmbStatus.getSelectedItem();
            String rating = txtRating.getText();

            try{
                Statement statement = (Statement) koneksi.getConnection().createStatement();
                statement.executeUpdate("insert into data_anime VALUES (null, '" + judul + "','" 
                            + tipe +"','"+ episode +"','"+ genre +"','"+ status +"','"+ rating +"')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");

                refresh();
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

